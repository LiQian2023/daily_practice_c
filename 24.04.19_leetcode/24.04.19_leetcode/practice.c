//提示：
//0 <= nums.length <= 100
//0 <= nums[i] <= 50
//0 <= val <= 100
int cmp(const void* p1, const void* p2) {
	return *(int*)p2 - *(int*)p1;
}
int removeElement(int* nums, int numsSize, int val) {
	if (numsSize == 0)
		return 0;
	for (int i = 0; i < numsSize; i++) {
		if (nums[i] == val)
			nums[i] = -1;
	}
	qsort(nums, numsSize, sizeof(int), cmp);
	int ans = numsSize - 1;
	while (ans >= 0 && nums[ans] == -1) {
		ans--;
	}
	return ans + 1;
}
